def add(num1, num2=0):
    return num1 + num2

print(add(2,2))

plus = lambda num1, num2: num1 + num2

print(plus(4,4))


def sub(num1, num2=0):
    return num1 - num2

print(sub(2,2))


def multipy(num1, num2):
    return num1 * num2

print(multipy(2,2))


def divide(num1, num2):
    return num1 / num2

print(divide(4,2))


